public class SkillModule {
	private int skillModuleId;
	private String skillModuleName;
	private String skillModuleNote;
	
	public int getSkillModuleId() {
		return skillModuleId;
	}
	public void setSkillModuleId(int skillModuleId) {
		this.skillModuleId = skillModuleId;
	}
	
	public String getSkillModuleName() {
		return skillModuleName;
	}
	public void setSkillModuleName(String skillModuleName) {
		this.skillModuleName = skillModuleName;
	}
	public String getSkillModuleNote() {
		return skillModuleNote;
	}
	public void setSkillModuleNote(String skillModuleNote) {
		this.skillModuleNote = skillModuleNote;
	}
	
	
	
	
}
